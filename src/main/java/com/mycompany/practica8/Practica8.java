    private void AbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbrirActionPerformed
        int res = fc.showOpenDialog(null);
        if(res == JFileChooser.APPROVE_OPTION){
            File fichero = fc.getSelectedFile();
            imagenOpened=true;
            try {
                for(JInternalFrame internalFrame: escritorio.getAllFrames()){
                internalFrame.dispose();
            }
                vI = new VentanaInterna();
                escritorio.add(vI);
                vI.setImg(fichero.getAbsolutePath());
                vI.setTitle(fichero.getName()); 
                vI.setLocation(new Point(escritorio.getX()+escritorio.getWidth()/2 - vI.getWidth()/2,
                escritorio.getY()+escritorio.getHeight()/2 - vI.getHeight()/2 + 10));
                
            } catch (IOException ex) {
                Logger.getLogger(Practica8.class.getName()).log(Level.SEVERE, null, ex);
            }
            repaint();
        }    
    }//GEN-LAST:event_AbrirActionPerformed

    private void umbralizarBtnDialogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_umbralizarBtnDialogActionPerformed
        String umbralTxt = umbral.getText();
        if(umbralTxt.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un umbral","Selecciona un umbral",2);
        }else if(!imagenOpened){
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar una imagen","Selecciona una imagen",2);
        }else{
            vI = new VentanaInterna();
            escritorio.add(vI);
            vI.setLocation(new Point(escritorio.getX()+escritorio.getWidth()/2 - vI.getWidth()/2,
                escritorio.getY()+escritorio.getHeight()/2 - vI.getHeight()/2 + 10));
            vI.umbralizar(Integer.parseInt(umbralTxt));
            vI.toFront();
            repaint();
        }

    }//GEN-LAST:event_umbralizarBtnDialogActionPerformed

