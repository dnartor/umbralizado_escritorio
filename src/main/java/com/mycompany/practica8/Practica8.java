    class myListener implements ComponentListener{

        @Override
        public void componentResized(ComponentEvent e) {
            if(escritorio.getAllFrames() != null){
                for(JInternalFrame internalFrame: escritorio.getAllFrames()){
                    recoloca(internalFrame);
                }
            }
            
        }
        private void recoloca(JInternalFrame internalFrame){
            if(internalFrame.getX()> escritorio.getX() + escritorio.getWidth() || 
                    internalFrame.getY()> escritorio.getY() + escritorio.getHeight()){

                internalFrame.setLocation(new Point(escritorio.getX()+escritorio.getWidth()/2 - internalFrame.getWidth()/2,
                        escritorio.getY()+escritorio.getHeight()/2- internalFrame.getHeight()/2 + 35));
            }
        }

        @Override
        public void componentMoved(ComponentEvent e) {
        }

        @Override
        public void componentShown(ComponentEvent e) {
        }

        @Override
        public void componentHidden(ComponentEvent e) {
        }   
    }
